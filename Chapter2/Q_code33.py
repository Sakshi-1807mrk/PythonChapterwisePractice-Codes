# for loop

fruits=["apple","mango","orange","cherry"]
# The loop will iterate over the fruits list and assign each value to the fruit variable. 
for fruit in fruits :
    print(fruit)#executes the print statement for each one.
print("-------------------------------------")

# using range() function for iterating over a sequence of numbers
for x in range(6): # range() generates value from 0 to n-1 here 5
    print(x)
print("-------------------------------------")

for y in range(2,10):
    print(y) # generates value from n to n-1
