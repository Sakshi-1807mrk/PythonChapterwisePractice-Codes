# string slicing

fruit='banana'
print(fruit[:6]) #slicing from start
print(fruit[3:]) #slicing till end
print(fruit[2:6]) #slicing in between
print(fruit[-6:]) #slicing using negative index
print("-------------")
# negative slicing
a='Sakshi'
print(a[-4:-2])

# slicing with skip values

a='Mango'
print(a[2:3:5])